package editor;

public class myPixel {
    private int red;

    private int green;

    private int blue;

    public  myPixel(){

        red = 0;

        green = 0;

        blue = 0;

    }

    public int getRed() {

        return red;

    }

    public void setRed(int red) {

        this.red = red;

    }

    public int getGreen() {

        return green;

    }

    public void setGreen(int green) {

        this.green = green;

    }

    public int getBlue() {

        return blue;

    }

    public void setBlue(int blue) {

        this.blue = blue;

    }
}
